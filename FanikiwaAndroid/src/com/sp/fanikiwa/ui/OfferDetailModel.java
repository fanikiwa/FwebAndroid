package com.sp.fanikiwa.ui;

public class OfferDetailModel {
private String Offerer;
private String OffererPhoto;
private String CRR;
private double ROI;
private String OfferDescription;

public String getOfferer() {
	return Offerer;
}
public void setOfferer(String offerer) {
	Offerer = offerer;
}
public String getOffererPhoto() {
	return OffererPhoto;
}
public void setOffererPhoto(String offererPhoto) {
	OffererPhoto = offererPhoto;
}
public String getCRR() {
	return CRR;
}
public void setCRR(String cRR) {
	CRR = cRR;
}
public double getROI() {
	return ROI;
}
public void setROI(double rOI) {
	ROI = rOI;
}
public String getOfferDescription() {
	return OfferDescription;
}
public void setOfferDescription(String offerDescription) {
	OfferDescription = offerDescription;
}
}
